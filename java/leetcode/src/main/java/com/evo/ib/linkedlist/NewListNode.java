package com.evo.ib.linkedlist;

public class NewListNode {
    public int val;
    public NewListNode next;
    public NewListNode arbitrary;

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public NewListNode getNext() {
        return next;
    }

    public void setNext(NewListNode next) {
        this.next = next;
    }

    public NewListNode getArbitrary() {
        return arbitrary;
    }

    public void setArbitrary(NewListNode arbitrary) {
        this.arbitrary = arbitrary;
    }
}

